
public class Wizard {

    String name;
    int credits;

    public Wizard(String name, int credits) {
    
        this.name = name;
        this.credits = credits;
    
    }

    public String getName() {
    
        return name;
    
    }

    public int getCredits() {
    
        return credits;
    
    }

    public void setCredits(int credits) {
    
        this.credits = credits;
    
    }

    public String toString() {
    
        return name;
    
    }

}
